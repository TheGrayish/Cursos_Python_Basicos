#List: es una coleccion que esta ordenada y cambiable. Perimite duplicar miembros

miList=["manzana", "pera", "uvas"]
print(miList)

#Tupla: es una coleccion que esta ordenad e inmutable. Permite duplicar miembors
miTupla = ("manzana", "pera", "uvas", "uvas")
print(miTupla)

#Set: es una coleccion que no esta ordenada que no esta indexada. No hay miembros duplicados.
miSet = {"manza","pera","uvas","uvas"}
print(miSet)

#Diccionario: es una coleccion que no esta ordenda, modificable e indexada.
miDiccionario = {"rojo":"manzana",
                 "verde":"pera",
                 "morado":"uvas"}
print(miDiccionario)
